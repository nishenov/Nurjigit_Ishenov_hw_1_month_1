public class CharacterPersonInfo {
    private int age;
    private String city;
    private String fraction;

    public CharacterPersonInfo(int age, String city, String fraction) {
        this.age = age;
        this.city = city;
        this.fraction = fraction;
    }

    public int getAge() {
        return age;
    }

    public String getCity() {
        return city;
    }

    public String getFraction() {
        return fraction;
    }
}
